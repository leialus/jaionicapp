export class DataTime {
	dia: number;
	mes: number;
	ano: number;
	hora: number;
	minuto: number;
	segundo: number;
	
	constructor(DIA: number, MES: number, ANO: number, HORA: number, MINUTO: number, SEGUNDO: number){
		
		this.dia = DIA;
		this.mes = MES;
		this.ano = ANO;
		this.hora = HORA;
		this.minuto = MINUTO;
		this.segundo = SEGUNDO;
		
	}
	
}
